import Foundation

func sumar (_ valorA:Int, _ valorB:Int) -> Int?
{
    var resultado:Int? = valorA + valorB
    return resultado
}

func resta (_ valorA:Int, _ valorB:Int) -> Int?
{
    var resultado:Int? = valorA - valorB
    return resultado
}

func multi (_ valorA:Int, _ valorB:Int) -> Int?
{
    var resultado:Int? = valorA * valorB
    return resultado
}

func div (_ valorA:Double, _ valorB:Double) -> Double?
{
    var resultado:Double? = valorA / valorB
    return resultado
}
